int periodValue = 10;

int compute_duty(int duty){
    //clamp to valid values
    if(duty > 100){
        duty = 100;
    } else if (duty < 0){
        duty = 0;
    }
    return periodValue * duty;
}

void left(bool reverse, int duty){
    engage_right_motor(reverse, duty);
    engage_left_motor(!reverse, duty);
}

void right(bool reverse, int duty){
    engage_left_motor(reverse, duty);
    engage_right_motor(!reverse, duty);
}

void engage_left_motor(bool reverse, int duty){
    int period = compute_duty(duty);
    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, period);

    //PA0, PA1
    if(reverse) {
        _a1(LOW);
        _a2(HIGH);
    } else {
        _a1(HIGH);
        _a2(LOW);
    }
}

void engage_right_motor(bool reverse, int duty){
    int period = compute_duty(duty);
    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, period);

    if(reverse) {
        _a3(LOW);
        _a4(HIGH);
    } else {
        _a3(HIGH);
        _a4(LOW);
    }
}

void stop(){
    _a1(LOW);
    _a2(LOW);
    _a3(LOW);
    _a4(LOW);
}

